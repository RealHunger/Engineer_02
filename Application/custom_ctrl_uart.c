/******************************************************************************************
 *                                   独立实现 CRC16 校验函数（完全保留）
 ******************************************************************************************/
static uint16_t Custom_CRC16_Check(uint8_t *p_msg, uint16_t len)
{
    uint16_t crc16 = 0xFFFF;  // CRC16初始值（协议附录一规定）
    while (len--)
    {
        uint8_t ch_data = *p_msg++;
        crc16 = (crc16 >> 8) ^ crc16_tab[(crc16 ^ ch_data) & 0x00FF];
    }
    return crc16;
}

