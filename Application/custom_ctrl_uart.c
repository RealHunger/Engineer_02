/******************************************************************************************
 *                                   核心函数：UART1发送7个关节弧度 + 1个夹爪开合状态（修改发送链路）
 ******************************************************************************************/
int CustomCtrl_UART_Send_MotorData(struct uart_device *uart_dev, float joint_angles_rad[JOINT_COUNT], uint8_t gripper_open)
{
    // 1. 参数合法性校验（替换为UART设备句柄校验）
    if (uart_dev == NULL || joint_angles_rad == NULL) {
        //LED_RED_Toggle();  // 可选：参数错误指示灯
        return -1;
    }

    // 2. 初始化发送帧（39字节：frame_header(5B)+cmd_id(2B)+data(30B)+frame_tail(2B)）
    uint8_t send_frame[TOTAL_FRAME_LEN] = {0};

    /******************************************************************************************
     * 3. 填充帧头（严格遵循RoboMaster 2026协议表1-2，完全保留）
     ******************************************************************************************/
    send_frame[0] = SOF_VALUE;                                  // 第0字节：SOF固定0xA5
    send_frame[1] = (uint8_t)(DATA_LEN_MOTOR_ANGLE & 0xFF);     // 第1字节：data_length低字节（30=0x1E）
    send_frame[2] = (uint8_t)((DATA_LEN_MOTOR_ANGLE >> 8) & 0xFF); // 第2字节：data_length高字节
    send_frame[3] = s_seq++;                                   // 第3字节：包序号自增（0~255循环）
    send_frame[4] = FrameHeader_CRC8_Calc(send_frame);          // 第4字节：帧头CRC8校验

    /******************************************************************************************
     * 4. 填充命令码（0x0302，图传链路，自定义控制器→机器人，协议表1-4，完全保留）
     ******************************************************************************************/
    send_frame[5] = (uint8_t)(CUSTOM_CMD_ID & 0xFF);            // 命令码低字节（小端存储）
    send_frame[6] = (uint8_t)((CUSTOM_CMD_ID >> 8) & 0xFF);     // 命令码高字节

    /******************************************************************************************
     * 5. 填充数据段（30字节，协议要求）：7个关节float（28B）+ 夹爪bool（1B）+ 填充（1B），完全保留
     ******************************************************************************************/
    // ① 复制7个关节弧度值（float[7]，28字节，偏移7~34字节）
    memcpy(&send_frame[7], joint_angles_rad, 7 * sizeof(float));
    // ② 填充夹爪开合状态（1字节，偏移35字节：0=闭合，1=打开）
    send_frame[7 + 7*4] = gripper_open ? 0x01 : 0x00;
    // ③ 剩余1字节填充0x00（偏移36字节），凑满30字节
    send_frame[7 + 7*4 + 1] = 0x00;

    /******************************************************************************************
     * 6. 填充帧尾（CRC16整包校验，协议表1-1，校验范围：frame_header+cmd_id+data，完全保留）
     ******************************************************************************************/
    uint16_t crc16 = Custom_CRC16_Check(send_frame, FRAME_HEADER_LEN + CMD_ID_LEN + DATA_LEN_MOTOR_ANGLE);
    send_frame[TOTAL_FRAME_LEN - 2] = (uint8_t)(crc16 & 0xFF);  // CRC16低字节
    send_frame[TOTAL_FRAME_LEN - 1] = (uint8_t)((crc16 >> 8) & 0xFF); // CRC16高字节

    /******************************************************************************************
     * 7. UART1发送（替换原USB CDC发送，适配你的UART设备驱动）
     ******************************************************************************************/
    // 调用UART设备的Send函数，发送二进制帧（注意：不是Print打印字符串）
    int send_len = uart_dev->Send(uart_dev, (char *)send_frame, TOTAL_FRAME_LEN, 100);
    if (send_len != TOTAL_FRAME_LEN) {
        //LED_RED_Toggle();  // 可选：发送失败指示灯
        return -2;
    } else {
        //LED_GREEN_Toggle();// 可选：发送成功指示灯
        return 0;
    }
}

